DROP TABLE IF EXISTS results CASCADE;


CREATE TABLE results (
  id SERIAL PRIMARY KEY NOT NULL,
  user_id INTEGER REFERENCES users(id) ON DELETE CASCADE NOT NULL,
  quiz_id INTEGER,
  result SMALLINT,
  grade VARCHAR(10)
);



